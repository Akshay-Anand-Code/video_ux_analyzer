export default function JiraTicketModal({ issue, onClose }: Props) {
  const open = issue !== null;

  // Keep last non-null issue so the modal retains its content while animating closed.
  const [cached, setCached] = useState<ValidatedIssue | null>(issue);
  if (issue && issue.id !== cached?.id) setCached(issue);
  const current = issue ?? cached;

  const [summary, setSummary] = useState("");
  const [issueType, setIssueType] = useState<IssueType>("Bug");
  const [priority, setPriority] = useState<Priority>("Medium");
  const [labels, setLabels] = useState<string[]>([]);
  const [description, setDescription] = useState("");
  const [newLabel, setNewLabel] = useState("");

  // Reset form state whenever a new issue opens the modal.
  useEffect(() => {
    if (!issue) return;
    setSummary(defaultSummary(issue.description));
    setIssueType(verdictToIssueType(issue.verdict));
    setPriority(severityToPriority(issue.severity));
    setLabels([issue.category]);
    setDescription(buildDescription(issue));
    setNewLabel("");
  }, [issue]);

  const handleAddLabel = () => {
    const trimmed = newLabel.trim().toLowerCase().replace(/\s+/g, "_");
    if (trimmed && !labels.includes(trimmed)) {
      setLabels([...labels, trimmed]);
    }
    setNewLabel("");
  };

  const handleRemoveLabel = (label: string) => {
    setLabels(labels.filter((l) => l !== label));
  };

  const handleSubmit = () => {
    const url = new URL(
      `${JIRA_BASE_URL}/secure/CreateIssueDetails!init.jspa`
    );
    url.searchParams.set("pid", JIRA_PROJECT_ID);
    url.searchParams.set("issuetype", issueType);
    url.searchParams.set("summary", summary);
    url.searchParams.set("description", description);
    url.searchParams.set("priority", priority);
    if (labels.length) url.searchParams.set("labels", labels.join(","));
    window.open(url.toString(), "_blank", "noopener,noreferrer");
    onClose();
  };

  const pCfg = PRIORITY_CONFIG[priority];
  const tCfg = ISSUE_TYPE_CONFIG[issueType];

  const anchorSeconds = current?.timestamp
    ? parseTimestamp(current.timestamp)
    : 0;
  const anchorLabel = current?.timestamp ?? "0:00";

  return (
    <Dialog open={open} onOpenChange={(o) => !o && onClose()}>
      <DialogContent
        showCloseButton={false}
        className="p-0 gap-0"
      >
        {/* Header with Jira breadcrumb */}
        <div className="flex items-center gap-3 px-6 py-4 border-b border-border bg-card/60">
          <JiraIcon />
          <div className="flex-1 min-w-0">
            <div className="flex items-center gap-1.5 text-xs text-muted-foreground">
              <span className="text-[#4c9aff] font-medium">UX Project</span>
              <span className="text-muted-foreground/60">/</span>
              <span>Analysis</span>
            </div>
            <DialogTitle className="text-sm mt-0.5">Create Issue</DialogTitle>
            <DialogDescription className="sr-only">
              Review and edit the Jira ticket details before creation.
            </DialogDescription>
          </div>
        </div>

        <div className="flex-1 overflow-y-auto px-6 py-5">
          <Tabs defaultValue="details" className="w-full">
            <TabsList>
              <TabsTrigger value="details">Details</TabsTrigger>
              <TabsTrigger value="attachments" className="gap-1.5">
                <Paperclip className="w-3 h-3" />
                Attachments
              </TabsTrigger>
            </TabsList>

            <TabsContent value="details" className="space-y-5">
              {/* Summary */}
              <div>
                <label className="block text-xs font-semibold text-muted-foreground mb-1.5">
                  Summary <span className="text-red-400">*</span>
                </label>
                <input
                  type="text"
                  value={summary}
                  onChange={(e) => setSummary(e.target.value)}
                  className="w-full px-3 py-2 bg-background border border-border rounded-lg text-sm text-foreground focus:border-primary focus:ring-1 focus:ring-primary focus:outline-none transition-colors"
                />
              </div>

              {/* Type & Priority row */}
              <div className="grid grid-cols-2 gap-4">
                <div>
                  <label className="block text-xs font-semibold text-muted-foreground mb-1.5">
                    Issue Type
                  </label>
                  <div className="flex items-center gap-2 px-3 py-2 bg-background border border-border rounded-lg focus-within:border-primary transition-colors">
                    <span
                      className={`w-5 h-5 rounded flex items-center justify-center text-[10px] text-white ${tCfg.bg}`}
                    >
                      {tCfg.icon}
                    </span>
                    <select
                      value={issueType}
                      onChange={(e) =>
                        setIssueType(e.target.value as IssueType)
                      }
                      className="flex-1 bg-transparent text-sm text-foreground focus:outline-none appearance-none cursor-pointer"
                    >
                      <option value="Bug" className="bg-card">Bug</option>
                      <option value="Task" className="bg-card">Task</option>
                      <option value="Spike" className="bg-card">Spike</option>
                      <option value="Story" className="bg-card">Story</option>
                    </select>
                    <ChevronDown className="w-4 h-4 text-muted-foreground" />
                  </div>
                </div>
                <div>
                  <label className="block text-xs font-semibold text-muted-foreground mb-1.5">
                    Priority
                  </label>
                  <div
                    className={`flex items-center gap-2 px-3 py-2 border rounded-lg transition-colors ${pCfg.wrap}`}
                  >
                    <span
                      className={`font-bold text-base leading-none ${pCfg.iconColor}`}
                    >
                      {pCfg.icon}
                    </span>
                    <select
                      value={priority}
                      onChange={(e) => setPriority(e.target.value as Priority)}
                      className="flex-1 bg-transparent text-sm focus:outline-none appearance-none cursor-pointer font-medium"
                    >
                      <option value="Highest" className="bg-card text-foreground">Highest</option>
                      <option value="High" className="bg-card text-foreground">High</option>
                      <option value="Medium" className="bg-card text-foreground">Medium</option>
                      <option value="Low" className="bg-card text-foreground">Low</option>
                      <option value="Lowest" className="bg-card text-foreground">Lowest</option>
                    </select>
                    <ChevronDown className={`w-4 h-4 ${pCfg.iconColor}`} />
                  </div>
                </div>
              </div>

              {/* Labels */}
              <div>
                <label className="block text-xs font-semibold text-muted-foreground mb-1.5">
                  Labels
                </label>
                <div className="flex flex-wrap items-center gap-2 px-3 py-2 bg-background border border-border rounded-lg min-h-[2.5rem] focus-within:border-primary transition-colors">
                  {labels.map((l) => (
                    <span
                      key={l}
                      className={`inline-flex items-center gap-1.5 px-2 py-0.5 rounded-md text-xs font-medium border ${labelColor(l)}`}
                    >
                      {l.replace(/_/g, " ")}
                      <button
                        type="button"
                        onClick={() => handleRemoveLabel(l)}
                        className="hover:text-foreground leading-none opacity-60 hover:opacity-100 transition-opacity"
                        aria-label={`Remove ${l}`}
                      >
                        ×
                      </button>
                    </span>
                  ))}
                  <input
                    type="text"
                    value={newLabel}
                    onChange={(e) => setNewLabel(e.target.value)}
                    onKeyDown={(e) => {
                      if (e.key === "Enter") {
                        e.preventDefault();
                        handleAddLabel();
                      } else if (
                        e.key === "Backspace" &&
                        !newLabel &&
                        labels.length
                      ) {
                        handleRemoveLabel(labels[labels.length - 1]);
                      }
                    }}
                    onBlur={handleAddLabel}
                    placeholder={labels.length ? "" : "Add label..."}
                    className="flex-1 min-w-[80px] bg-transparent text-xs text-foreground focus:outline-none"
                  />
                </div>
              </div>

              {/* Description */}
              <div>
                <label className="block text-xs font-semibold text-muted-foreground mb-1.5">
                  Description
                </label>
                <textarea
                  value={description}
                  onChange={(e) => setDescription(e.target.value)}
                  rows={9}
                  className="w-full px-3 py-2 bg-background border border-border rounded-lg text-xs text-foreground font-mono leading-relaxed focus:border-primary focus:ring-1 focus:ring-primary focus:outline-none resize-y"
                />
              </div>

              {/* Reporter / Assignee */}
              <div className="grid grid-cols-2 gap-4">
                <div>
                  <label className="block text-xs font-semibold text-muted-foreground mb-1.5">
                    Reporter
                  </label>
                  <div className="flex items-center gap-2 px-3 py-2 bg-background border border-border rounded-lg">
                    <div className="w-6 h-6 rounded-full bg-gradient-to-br from-violet-500 to-violet-700 flex items-center justify-center text-xs font-bold text-white">
                      U
                    </div>
                    <span className="text-sm text-foreground">
                      UX Analyzer Bot
                    </span>
                  </div>
                </div>
                <div>
                  <label className="block text-xs font-semibold text-muted-foreground mb-1.5">
                    Assignee
                  </label>
                  <div className="flex items-center gap-2 px-3 py-2 bg-background border border-border rounded-lg">
                    <div className="w-6 h-6 rounded-full bg-muted border border-dashed border-muted-foreground/50 flex items-center justify-center text-xs text-muted-foreground">
                      ?
                    </div>
                    <span className="text-sm text-muted-foreground">
                      Unassigned
                    </span>
                  </div>
                </div>
              </div>
            </TabsContent>

            <TabsContent value="attachments">
              <SnippetMock
                anchorSeconds={anchorSeconds}
                anchorLabel={anchorLabel}
              />
            </TabsContent>
          </Tabs>
        </div>

        {/* Footer */}
        <div className="px-6 py-4 border-t border-border bg-card/60 flex items-center justify-between gap-3">
          <p className="text-xs text-muted-foreground hidden sm:flex items-center gap-1.5">
            <ExternalLink className="w-3 h-3" />
            Opens Jira in a new tab with fields pre-filled
          </p>
          <div className="flex gap-2 ml-auto">
            <Button variant="ghost" onClick={onClose}>
              Cancel
            </Button>
            <Button onClick={handleSubmit} disabled={!summary.trim()}>
              Create Issue
            </Button>
          </div>
        </div>
      </DialogContent>
    </Dialog>
  );
}
