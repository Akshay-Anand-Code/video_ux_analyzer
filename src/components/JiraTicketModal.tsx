const ISSUE_TYPE_CONFIG: Record<IssueType, { bg: string; icon: string }> = {
  Bug: { bg: "bg-red-500", icon: "●" },
  Task: { bg: "bg-blue-500", icon: "✓" },
  Spike: { bg: "bg-purple-500", icon: "◆" },
  Story: { bg: "bg-green-500", icon: "▲" },
};

const CATEGORY_COLORS: Record<string, string> = {
  navigation: "bg-purple-50 text-purple-700 border-purple-200",
  ui_element: "bg-blue-50 text-blue-700 border-blue-200",
  user_flow: "bg-emerald-50 text-emerald-700 border-emerald-200",
  accessibility: "bg-pink-50 text-pink-700 border-pink-200",
  interaction_delay: "bg-orange-50 text-orange-700 border-orange-200",
  unclear_messaging: "bg-teal-50 text-teal-700 border-teal-200",
};

function labelColor(label: string): string {
  return (
    CATEGORY_COLORS[label] ??
    "bg-slate-50 text-slate-700 border-slate-200"
  );
}

function verdictToIssueType(verdict: ValidatedIssue["verdict"]): IssueType {
  if (verdict === "genuine_bug") return "Bug";
  if (verdict === "stylistic_choice") return "Task";
  return "Spike";
}

function severityToPriority(severity: ValidatedIssue["severity"]): Priority {
  if (severity === "critical") return "Highest";
  if (severity === "major") return "High";
  return "Medium";
}

function defaultSummary(description: string): string {
  const first = description.split(/(?<=[.!?])\s/)[0]?.trim() ?? description;
  if (first.length <= 80) return first;
  return first.slice(0, 79).trimEnd() + "…";
}

function buildDescription(issue: ValidatedIssue): string {
  const lines: string[] = [];
  lines.push("h2. Observed issue");
  lines.push(issue.description);
  lines.push("");
  lines.push("h2. Category");
  lines.push(issue.category.replace(/_/g, " "));
  lines.push("");
  lines.push("h2. AI Critic Verdict");
  lines.push(
    `*${issue.verdict.replace(/_/g, " ")}* — ${issue.criticReasoning}`
  );
  if (issue.timestamp) {
    lines.push("");
    lines.push("h2. Reproduce at");
    lines.push(issue.timestamp);
  }
  lines.push("");
  lines.push("----");
  lines.push("_Generated by Video UX Analyzer_");
  return lines.join("\n");
}

function parseTimestamp(ts: string): number {
  const parts = ts.split(":").map(Number);
  if (parts.length === 3)
    return (parts[0] ?? 0) * 3600 + (parts[1] ?? 0) * 60 + (parts[2] ?? 0);
  if (parts.length === 2) return (parts[0] ?? 0) * 60 + (parts[1] ?? 0);
  return parseFloat(ts) || 0;
}

function formatTimestamp(seconds: number): string {
  const total = Math.max(0, Math.floor(seconds));
  const h = Math.floor(total / 3600);
  const m = Math.floor((total % 3600) / 60);
  const s = total % 60;
  if (h > 0)
    return `${h}:${String(m).padStart(2, "0")}:${String(s).padStart(2, "0")}`;
  return `${m}:${String(s).padStart(2, "0")}`;
}

